
public class DemoBitwise {
    public static void main(String[] args) {
        int x = 10, y = 20;
        System.out.println("\n (x&y) = " + (x & y));
        System.out.println("(x|y) = " + (x | y));
        System.out.println("\n (x^y) = " + (x^y));
        System.out.println("\n (x<<2) is " + (x<<2));
        System.out.println("\n (x>>2) is " +(x>>2));

    }
}
